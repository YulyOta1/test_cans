unsigned short Crc16(unsigned char* routingTable)
{
    unsigned short crc = 0xFFFF;
    unsigned char i;
    unsigned short len = 3000;
    while (len--) {
        crc ^= *routingTable++ << 8;
        for (i = 0; i < 8; i++)
            crc = crc & 0x8000 ? (crc << 1) ^ 0x1021 : crc << 1;
    }
    return crc;
}
